public class Ex112{
  public static void main(String[] args){

    System.out.println("Exercise112 インクリメント演算子");
    for (int i=0; i<=10; i++){
      System.out.println("i= " + i);
    }

    System.out.println("Exercise112 デクリメント演算子");
    for (int i=10; i>=0; i--){
      System.out.println("i= " + i);
    }
  }

}

